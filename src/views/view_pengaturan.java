    private void resetForm() {
        tusername.setText("");
        tpassword.setText("");
        tusername.requestFocus();
    }

    private void loadTableUser() {

        DefaultTableModel model = new DefaultTableModel();

        model.addColumn("ID");
        model.addColumn("Username");

        UserDAO dao = new UserDAO();

        for (models.User user : dao.getAll()) {

            model.addRow(new Object[]{
                user.getIdUser(),
                user.getUsername(),});
        }

        tblUser.setModel(model);
    }

    private void prosesRegister() {
        String username = tusername.getText().trim();
        String password = new String(tpassword.getPassword()).trim();

        if (username.isEmpty()) {

            JOptionPane.showMessageDialog(this,
                    "Username tidak boleh kosong");

            return;
        }

        if (password.isEmpty()) {

            JOptionPane.showMessageDialog(this,
                    "Password tidak boleh kosong");

            return;
        }

        UserDAO dao = new UserDAO();

        if (dao.cekUsername(username)) {

            JOptionPane.showMessageDialog(this,
                    "Username sudah digunakan");

            return;
        }

        models.User user = new models.User();

        user.setUsername(username);
        user.setPassword(password);

        boolean berhasil = dao.register(user);

        if (berhasil) {

            JOptionPane.showMessageDialog(this,
                    "Registrasi berhasil");

            loadTableUser();
            resetForm();

        } else {

            JOptionPane.showMessageDialog(this,
                    "Registrasi gagal");

        }
    }

